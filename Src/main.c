// Calculation of duty cycle of the pwm of ESP8266 using Input Capture Compare.
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Channel==HAL_TIM_ACTIVE_CHANNEL_1)
	{
		Capture = HAL_TIM_ReadCapturedValue(&htim1,TIM_CHANNEL_1);
		clk1 = HAL_RCC_GetPCLK2Freq();
		clk2 = HAL_RCC_GetPCLK1Freq();
		clk3 = HAL_RCC_GetHCLKFreq();
		COUNT = HAL_RCC_GetPCLK2Freq()/(htim1.Instance->PSC + 1);
		Freq = COUNT/Capture;
  
	}
	if (captures[1] >= captures[0])
		diffCapture = captures[1] - captures[0];
	else
		diffCapture = (htim1.Instance->ARR - captures[0]) + captures[1];
	frequency = HAL_RCC_GetPCLK1Freq() / (htim1.Instance->PSC + 1);
	frequency = (float) frequency / diffCapture;
	 /* Get the Input Capture value */
	captures[0] = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
	captures[1] = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_2);
	ccr1 = TIM1->CCR1;
	if (captures[0] != 0)
	{
		/* Duty cycle computation */
		DutyCycle = ( captures[1] * 100)/captures[0];
		frequency = (HAL_RCC_GetHCLKFreq())/captures[0];
	}
	else
	{
		DutyCycle = 0;
		frequency = 0;
	}
}
