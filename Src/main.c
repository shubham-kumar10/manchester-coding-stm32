static void UART_encode()
{
	tmp = data[0];
	for (i = 0; i < 2; i++) 
	{
		encoded[i] = 0;    
		for (j = 0; j < 4; j++) 
		{
			encoded[i] >>= 2;
			if (tmp & 0x01)
					encoded[i] |= 0x40;  
			else
					encoded[i] |= 0x80;  
			tmp >>= 1;
		}
		HAL_UART_Transmit_IT(&huart4,(uint8_t *)encoded,2);
	}
}
static void UART_decode()
{
	HAL_UART_Receive_IT(&huart4,(uint8_t *)rx,2);
	for(k=0;k<2;k++)
	{
		tmp = rx[k];
		for (l=0;l<4;l++)
		{
			x++;
			decoded >>= 1;
			pattern = tmp & 0x03;
			if (pattern == 0x01)  
				decoded |= 0x08;
			else if(pattern == 0x02)  
					decoded &= 0xF7;
			tmp >>= 2;
		}
		if(k%2==0)
			msg = decoded;
		else
			msg = msg<<8|decoded;
	}
}
